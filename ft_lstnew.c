#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*ptr;

	ptr = (t_list *)malloc(sizeof(t_list));
	if (!ptr)
		return (NULL);
	if (!content)
		ptr -> content = NULL;
	else
		ptr -> content = content;
	ptr -> next = NULL;
	return (ptr);
}
